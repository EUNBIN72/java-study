package sec02.exam04.quiz;

public class Calculator {
	int add;
	int multiple;
	int divide;
	
	public int add (int num1, int num2) {
		return  num1 + num2;
	}
	
	public int multiple (int num1, int num2) {
		return num1 * num2;
	}
	
	public int divide (int num1, int num2) {
		return num1 / num2;
	}

}
